# Sorting a List Temporarily with the sorted() Function alphabetically, p.44
print('\n')
cars = ['bmw', 'audi', 'toyota', 'subaru']

print("Here is the orginal list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the sorted list in reverse:")
print(sorted(cars, reverse=True))

print("\nHere is the orginal list again:")
print(cars)
print('\n')


